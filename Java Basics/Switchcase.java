public class Switchcase {
    public static void main(String[] args) {
        int n = 1;
        switch (n) {
            case 5:
                System.out.println("five");
                break;
            case 6:
                System.out.println("six");
                break;
            case 7:
                System.out.println("seven");
                break;
            default:
                System.out.println("kuch bhi");
                break;
        }
    }
}
